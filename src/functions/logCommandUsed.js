"use strict";

///////////////////////////////////////////////////////////////////////////////
//////// Imports/Variables
const color = require("./colors.js");

///////////////////////////////////////////////////////////////////////////////
//////// Log Usage of Commands
function logCommandUsed(interaction, command) {
  console.log(
    `${color.blue} Command Used: ${command} | From: ${interaction.user.username} ${color.reset}`
  );
}

///////////////////////////////////////////////////////////////////////////////
//////// Exports
module.exports = { logCommandUsed };
